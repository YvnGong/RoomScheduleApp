    private void CheckByDateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_CheckByDateActionPerformed
        // TODO add your handling code here:
        statusArea.setText("");
        String dateStatus = DateStatus.getSelectedItem().toString();
        Date datestatus = Date.valueOf(dateStatus);
        ArrayList<ReservationEntry>ReservationList = ReservationQueries.getReservationsByDate(datestatus);
        String row0 = String.format("%-20s %-20s %n", "Faculty", "Room");
        statusArea.append(row0);
        for (ReservationEntry r : ReservationList)
        {
            String row = String.format("%-20s %-20s %n", r.getFaculty(), r.getRoom());
            statusArea.append(row);
        }      
    }//GEN-LAST:event_CheckByDateActionPerformed

    private void checkWaitListActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_checkWaitListActionPerformed
        // TODO add your handling code here:
        waitListDisplay.setText("");
        ArrayList<WaitlistEntry> waitList = WaitlistQueries.getAllWaitList();
        String row0 = String.format("%-10s %-20s %-10s %-10s %n", "Faculty", "Date", "Seats", "Timestamp");
        waitListDisplay.append(row0);
        for (WaitlistEntry w : waitList)
        {
            String S = new SimpleDateFormat("MM/dd/yyyy HH:mm:ss").format(w.getTimestamp());
            String row = String.format("%-10s %-20s %-10s %-10s %n", w.getFaculty(), w.getDate(), w.getSeats(), S);
            waitListDisplay.append(row);
        }      
    }//GEN-LAST:event_checkWaitListActionPerformed

    private void CheckByFacultyActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_CheckByFacultyActionPerformed
        // TODO add your handling code here:
        statusArea.setText("");
        String facultyStatus = FacultyStatus.getSelectedItem().toString();
        ArrayList<ReservationEntry>ReservationList = ReservationQueries.getReservationsByFaculty(facultyStatus);
        ArrayList<WaitlistEntry>WaitListList = WaitlistQueries.getWaitListByFaculty(facultyStatus);
        String orow = String.format("%-20s %-20s %-20s %n", "Faculty", "Date", "Room");
        statusArea.append(orow);
        for (ReservationEntry r : ReservationList)
        {
            String row = String.format("%-20s %-20s %-20s %n", r.getFaculty(), r.getDate(), r.getRoom());
            statusArea.append(row);
        }
        String wrow = String.format("%s %n", "WaitList Status");
        String lrow = String.format("%-20s %-20s %-20s %n", "Faculty", "Date", "Seats");
        statusArea.append(wrow);
        statusArea.append(lrow);
        for (WaitlistEntry w : WaitListList)
        {
            String row = String.format("%-20s %-20s %-20s %n", w.getFaculty(), w.getDate(), w.getSeats());
            statusArea.append(row);
        } 
    }//GEN-LAST:event_CheckByFacultyActionPerformed

