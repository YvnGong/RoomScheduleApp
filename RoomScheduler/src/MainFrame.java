//sets all the JComboBox items in the program to the lists they are to hold
    public void populateLists()
    {
        selectFaculty.removeAllItems();
        selectDate.removeAllItems();
        DateStatus.removeAllItems();
        FacultyStatus.removeAllItems();
        cancelFaculty.removeAllItems();
        cancelDate.removeAllItems();
        RoomList.removeAllItems();
        
        ArrayList<String> facultyList = Faculty.getFaculty();
        ArrayList<String> dateList = Dates.getDates();
        ArrayList<String> allRoomList = RoomQueries.getAllRooms();
        
        for (String s : facultyList)
        {
            selectFaculty.addItem(s);
            FacultyStatus.addItem(s);
            cancelFaculty.addItem(s);
        }   
        
        for (String s : dateList)
        {
            selectDate.addItem(s);
            cancelDate.addItem(s);
            DateStatus.addItem(s);
        }   
                
        for (String s : allRoomList)
        {
            RoomList.addItem(s);
        }
    }
    
